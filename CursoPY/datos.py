#Operadores numericos

a = 10
b = 3
print(a + b) # 13
print(a - b) # 7
print(a * b) # 30
print(a / b) # 3.3333333333333335
print(a // b) # 3 (division entera)
print(a % b) # 1 (modulo)
print(a ** b) # 1000 (potencia)
print(a > b) # True
print(a < b) # False
print(a = b ) # False
print(a == b) # False
print(a != b) # True



